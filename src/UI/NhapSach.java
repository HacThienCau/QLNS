    private String createPNS(){
        String sql = "INSERT INTO phieunhapsach (TONGSLNHAP, TONGTIEN, NGAYNHAP) VALUES (?,?,?)";
        String ngay = NN.getText(); //dd/MM/yyyy
        DateTimeFormatter inputFormatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        DateTimeFormatter outputFormatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        LocalDate date = LocalDate.parse(ngay, inputFormatter);
        String formattedNgay = date.format(outputFormatter);
        Date nn = Date.valueOf(formattedNgay);
        Double tongtien = 0.0;
        int tongsl = 0;
        for(CTNhap sach : danhSachPhieuNhap){
            tongsl += sach.getSoLuong();
            tongtien += sach.getGia()*sach.getSoLuong();
        }
        try {
            ps = conn.prepareStatement(sql);
            ps.setInt(1, tongsl);
            ps.setDouble(2, tongtien);
            ps.setDate(3, nn);
            ps.executeUpdate();
            ps.close();
            sql = "select last_insert_id()";
            ps = conn.prepareStatement(sql);
            rs = ps.executeQuery();
            String mpn = null;
            if(rs.next()){
                mpn = rs.getString(1);
                JOptionPane.showMessageDialog(NhapSach.this, "Thêm phiếu nhập sách thành công");
            }
            return mpn;
        } catch (SQLException ex) {
            Logger.getLogger(NhapSach.class.getName()).log(Level.SEVERE, null, ex);
        }
        return null;
    }
    private void updateCTNS(String mpn){
        String sql = "INSERT INTO chitietnhapsach (MAPHIEUNHAP, MASACH, GIANHAP, SLNHAP) VALUES (?,?,?,?)";
        try {
            ps = conn.prepareStatement(sql);
            for(CTNhap sach : danhSachPhieuNhap){
                ps.setString(1, mpn);
                ps.setString(2, sach.getMaSach());
                ps.setDouble(3, sach.getGia());
                ps.setInt(4, sach.getSoLuong());
                ps.executeUpdate();
            }
        } catch (SQLException ex) {
            Logger.getLogger(NhapSach.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
