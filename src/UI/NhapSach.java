    private int getSLTon(String masach){
        String sql = "SELECT SLTON FROM SACH WHERE MASACH = ?";
        try {
            ps = conn.prepareStatement(sql);
            ps.setString(1, masach);
            rs = ps.executeQuery();
            while(rs.next())
            return rs.getInt("SLTon");
        } catch (SQLException ex) {
            Logger.getLogger(NhapSach.class.getName()).log(Level.SEVERE, null, ex);
        }        
        return -1;
    }
