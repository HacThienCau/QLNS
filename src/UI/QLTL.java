    private void loadTable1(){
      // create jTable1 columns
      DefaultTableModel model = new DefaultTableModel(){
          //không cho chỉnh sửa trong bảng
          @Override
      public boolean isCellEditable(int row, int column) {
          return false;
      }
      };

      String tieude[] = new String[]{"Mã thể loại", "Tên thể loại"};
      model.setColumnIdentifiers(tieude);
      String sql = "SELECT * FROM theloai";
        try {
            ps = conn.prepareStatement(sql);
            rs = ps.executeQuery();
            while(rs.next()){
                model.addRow(new Object[]{rs.getString("MATHELOAI"),rs.getString("TENTHELOAI")});
            }
            tb_phieunhap.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(QLTL.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
