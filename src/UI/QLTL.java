    private void SuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SuaActionPerformed
        String tentheloai = tx_tentheloai.getText();
        int selectedRow = tb_phieunhap.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(QLTL.this, "Vui lòng chọn một dòng");
            return;
        }
        if(tentheloai.isBlank()){
            JOptionPane.showMessageDialog(QLTL.this, "Vui lòng nhập tên thể loại");
            return;
        }
        String sql = "UPDATE THELOAI SET TENTHELOAI = ? WHERE MATHELOAI = ?";
        try {
            ps = conn.prepareStatement(sql);
            ps.setString(1, tentheloai);
            ps.setString(2, tb_phieunhap.getValueAt(selectedRow, 0).toString());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(QLTL.this, "Cập nhật thể loại thành công");
            loadTable1();
            tx_tentheloai.setText("");
        } catch (SQLException ex) {
            Logger.getLogger(QLTL.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_SuaActionPerformed

    private void XoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_XoaActionPerformed
        int selectedRow = tb_phieunhap.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(QLTL.this, "Vui lòng chọn một dòng");
            return;
        }
        String sql = "DELETE FROM THELOAI WHERE MATHELOAI = ?";
        try {
            ps = conn.prepareStatement(sql);
            ps.setString(1, tb_phieunhap.getValueAt(selectedRow, 0).toString());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(QLTL.this, "Xóa thể loại thành công");
            loadTable1();
            tx_tentheloai.setText("");
        } catch (SQLException ex) {
            if(ex.getMessage().contains("Cannot delete")){
                JOptionPane.showMessageDialog(QLTL.this, "Không thể xóa do vẫn còn dữ liệu liên quan");
            }
        }
    }//GEN-LAST:event_XoaActionPerformed

    private void ThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ThemActionPerformed
        String tentheloai = tx_tentheloai.getText();
        if(tentheloai.isBlank()){
            JOptionPane.showMessageDialog(QLTL.this, "Vui lòng nhập tên thể loại");
            return;
        }
        String sql = "INSERT INTO THELOAI (TENTHELOAI) VALUES (?)";
        try {
            ps = conn.prepareStatement(sql);
            ps.setString(1, tentheloai);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(QLTL.this, "Thêm thể loại thành công");
            loadTable1();
            tx_tentheloai.setText("");
        } catch (SQLException ex) {
            if(ex.getMessage().contains("Duplicate entry")){
                JOptionPane.showMessageDialog(QLTL.this, "Tên thể loại đã tồn tại");
            }
        }
    }//GEN-LAST:event_ThemActionPerformed

